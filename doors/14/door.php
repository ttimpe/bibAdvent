  <h1>Die Weihnachtsfahrt</h1>
<p>Denkt Euch, ich habe das Christkind gesehen,
ich hab's überfahren, es war ein Versehen.
Ich hatte gerade die Äuglein zu,
ich träumte beim Fahren in himmlischer Ruh.</p>

<p>Das Christkind hat in dieser heiligen Nacht
Bekanntschaft mit meinem Kühler gemacht.
Später sah ich auch noch den Weihnachtsmann,
er feuerte grad' seine Renntiere an.</p>

<p>Ich überholte den langsamen Wicht,
doch sah ich den Gegenverkehr dabei nicht.
Ich wich noch aus, doch leider nicht Santa,
ein kurzes Rumsen und er klebte am Manta.</p>

<p>Am Ende sah ich auch den Nikolaus,
er stürmte gerade aus dem Freudenhaus.
Er kam ganz hektisch über die Kreuzung gelaufen,
wollt am Automaten neue Präser sich kaufen.</p>

<p>Mein Auto und mich hat er wohl nicht gesehen,
jedenfalls blieben von ihm nur die Stiefel stehen.
So ist die Moral von dem Gedicht,
Fahr zu schnell Dein Auto nicht.</p>

<p>Denn als ich zuhause war, da musste ich heulen,
mein schöner Wagen, der hatte drei Beulen,
vom Christkind, vom Niklas und vom Santa Claus,
ja, dieses Jahr fällt Weihnachten dann wohl aus!</p>
